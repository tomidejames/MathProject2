public class AdvanceMath {
    
    public double squareRoot (double numberA){
        double a = Math. sqrt (numberA);
        return a; 
    }     
    public int Square (int numberB){
        int b = (numberB * numberB);
        return b;
    }
    public int multiplyAndRaise (int numberC, int numberD){
        int Multiply = numberC * numberD;
        int MultiplyAndSquare = Multiply * Multiply;
        return MultiplyAndSquare;
    }
        
    
    
    
}
